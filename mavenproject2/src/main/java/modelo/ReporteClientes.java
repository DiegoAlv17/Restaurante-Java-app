public class ReporteClientes {
    private String idboleta;
    private String nompla;
    private String cantpla;
    private String tipopago;

    /**
     * @return the idboleta
     */
    public String getIdboleta() {
        return idboleta;
    }

    /**
     * @param idboleta the idboleta to set
     */
    public void setIdboleta(String idboleta) {
        this.idboleta = idboleta;
    }

    /**
     * @return the nompla
     */
    public String getNompla() {
        return nompla;
    }

    /**
     * @param nompla the nompla to set
     */
    public void setNompla(String nompla) {
        this.nompla = nompla;
    }

    /**
     * @return the cantpla
     */
    public String getCantpla() {
        return cantpla;
    }

    /**
     * @param cantpla the cantpla to set
     */
    public void setCantpla(String cantpla) {
        this.cantpla = cantpla;
    }

    /**
     * @return the tipopago
     */
    public String getTipopago() {
        return tipopago;
    }

    /**
     * @param tipopago the tipopago to set
     */
    public void setTipopago(String tipopago) {
        this.tipopago = tipopago;
    }
    
    
}
